void solve(long long R, long long G, long long B, long long N){
    LL ans =0;
    for(int i=0; i<N; i++){
        for(int j=0; j<N; j++){
            // REP(k, N){
            //     if(R*i+G*j+B*k == N){
            //         ans++;
            //     }
            // }
            int k = (N-R*i-G*j) % B;
            if(k == 0)ans++;
        }
    }
    cout<<ans<<endl;
}

int main(){
    long long R;
    scanf("%lld",&R);
    long long G;
    scanf("%lld",&G);
    long long B;
    scanf("%lld",&B);
    long long N;
    scanf("%lld",&N);
    solve(R, G, B, N);
    return 0;
}
